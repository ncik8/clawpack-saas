'use client';

import Link from 'next/link';
import { usePathname } from 'next/navigation';
import { useState, useEffect } from 'react';
import { supabase } from '@/lib/supabase';

const navigation = [
  { name: 'Dashboard', href: '/dashboard', icon: HomeIcon },
  { name: 'Create', href: '/dashboard/create', icon: PlusIcon },
  { name: 'AI Image', href: '/dashboard/ai-image', icon: ImageIcon },
  { name: 'Calendar', href: '/dashboard/calendar', icon: CalendarIcon },
  { name: 'Connected', href: '/dashboard/connected-accounts', icon: LinkIcon },
  { name: 'Settings', href: '/dashboard/settings', icon: SettingsIcon },
];

export default function Sidebar() {
  const pathname = usePathname();
  const [user, setUser] = useState<any>(null);
  const [connectedCount, setConnectedCount] = useState(0);

  useEffect(() => {
    loadUser();
  }, []);

  const loadUser = async () => {
    const { data: { user } } = await supabase.auth.getUser();
    setUser(user);
    
    if (user) {
      // Count connected platforms
      const { data: connections } = await supabase
        .from('social_connections')
        .select('platform')
        .eq('user_id', user.id);
      
      setConnectedCount(connections?.length || 0);
    }
  };

  return (
    <aside className="fixed left-0 top-0 h-screen w-60 bg-[#111827] border-r border-[#374151] flex flex-col">
      {/* Logo */}
      <div className="p-5 border-b border-[#374151]">
        <Link href="/dashboard" className="flex items-center gap-2">
          <div className="w-8 h-8 rounded-md bg-gradient-to-br from-[#1780e3] to-[#76afe5] flex items-center justify-center">
            <span className="text-white font-bold text-sm">CP</span>
          </div>
          <span className="text-white font-semibold text-lg">ClawPack</span>
        </Link>
      </div>

      {/* Navigation */}
      <nav className="flex-1 p-4 space-y-1">
        {navigation.map((item) => {
          const isActive = pathname === item.href || pathname.startsWith(item.href + '/');
          return (
            <Link
              key={item.name}
              href={item.href}
              className={`flex items-center gap-3 px-3 py-2.5 rounded-md text-sm font-medium transition-colors ${
                isActive
                  ? 'bg-[#1780e3]/10 text-[#1780e3]'
                  : 'text-[#9ca3af] hover:text-white hover:bg-white/5'
              }`}
            >
              <item.icon className="w-5 h-5" />
              {item.name}
            </Link>
          );
        })}
      </nav>

      {/* Connected accounts preview */}
      <div className="p-4 border-t border-[#374151]">
        <p className="text-xs text-[#6b7280] uppercase tracking-wider mb-3">Connected</p>
        <div className="flex items-center gap-2">
          <div className="flex -space-x-2">
            <PlatformIcon platform="x" className="w-6 h-6" />
            <PlatformIcon platform="linkedin" className="w-6 h-6" />
          </div>
          <span className="text-xs text-[#9ca3af]">{connectedCount} platforms</span>
        </div>
      </div>

      {/* User */}
      <div className="p-4 border-t border-[#374151]">
        <div className="flex items-center gap-3">
          <div className="w-9 h-9 rounded-full bg-gradient-to-br from-[#1780e3] to-[#76afe5] flex items-center justify-center text-white font-medium text-sm">
            {user?.user_metadata?.display_name?.charAt(0).toUpperCase() || user?.email?.charAt(0).toUpperCase() || 'U'}
          </div>
          <div className="flex-1 min-w-0">
            <p className="text-sm font-medium text-white truncate">
              {user?.user_metadata?.display_name || user?.email?.split('@')[0] || 'User'}
            </p>
            <p className="text-xs text-[#6b7280]">Free Plan</p>
          </div>
          <button
            onClick={async () => {
              await supabase.auth.signOut();
              window.location.href = '/';
            }}
            className="p-2 text-[#6b7280] hover:text-white hover:bg-white/10 rounded-md transition-colors"
            title="Logout"
          >
            <LogoutIcon className="w-5 h-5" />
          </button>
        </div>
      </div>
    </aside>
  );
}

